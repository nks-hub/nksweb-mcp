import { McpServer } from "@modelcontextprotocol/sdk/server/mcp.js";
import { z } from "zod";
import { NksWebClient, truncateResponse } from "../client.js";

export function registerRedirectsTools(server: McpServer, client: NksWebClient): void {
  server.registerTool(
    "nksweb_list_redirects",
    {
      title: "List Redirects",
      description: "List all URL redirects",
      inputSchema: {},
      annotations: {
        readOnlyHint: true,
        destructiveHint: false,
        idempotentHint: true,
        openWorldHint: false,
      },
    },
    async () => {
      try {
        const data = await client.get("/redirects");
        return {
          content: [{ type: "text" as const, text: truncateResponse(data) }],
        };
      } catch (err) {
        const message = err instanceof Error ? err.message : String(err);
        return {
          content: [{ type: "text" as const, text: `Error: ${message}` }],
          isError: true,
        };
      }
    }
  );

  server.registerTool(
    "nksweb_get_redirect",
    {
      title: "Get Redirect",
      description: "Get a single URL redirect by ID",
      inputSchema: {
        id: z.number().describe("Redirect ID"),
      },
      annotations: {
        readOnlyHint: true,
        destructiveHint: false,
        idempotentHint: true,
        openWorldHint: false,
      },
    },
    async (args) => {
      try {
        const data = await client.get(`/redirects/${args.id}`);
        return {
          content: [{ type: "text" as const, text: truncateResponse(data) }],
        };
      } catch (err) {
        const message = err instanceof Error ? err.message : String(err);
        return {
          content: [{ type: "text" as const, text: `Error: ${message}` }],
          isError: true,
        };
      }
    }
  );

  server.registerTool(
    "nksweb_create_redirect",
    {
      title: "Create Redirect",
      description: "Create a new URL redirect",
      inputSchema: {
        oldUrl: z.string().describe("The source URL to redirect from"),
        newUrl: z.string().describe("The destination URL to redirect to"),
        statusCode: z.number().optional().default(301).describe("HTTP status code for the redirect (default: 301)"),
        active: z.boolean().optional().default(true).describe("Whether the redirect is active (default: true)"),
        note: z.string().optional().describe("Optional note or description for the redirect"),
      },
      annotations: {
        readOnlyHint: false,
        destructiveHint: false,
        idempotentHint: false,
        openWorldHint: false,
      },
    },
    async (args) => {
      try {
        const data = await client.post("/redirects", args);
        return {
          content: [{ type: "text" as const, text: truncateResponse(data) }],
        };
      } catch (err) {
        const message = err instanceof Error ? err.message : String(err);
        return {
          content: [{ type: "text" as const, text: `Error: ${message}` }],
          isError: true,
        };
      }
    }
  );

  server.registerTool(
    "nksweb_update_redirect",
    {
      title: "Update Redirect",
      description: "Update an existing URL redirect by ID",
      inputSchema: {
        id: z.number().describe("Redirect ID"),
        oldUrl: z.string().optional().describe("The source URL to redirect from"),
        newUrl: z.string().optional().describe("The destination URL to redirect to"),
        statusCode: z.number().optional().describe("HTTP status code for the redirect"),
        active: z.boolean().optional().describe("Whether the redirect is active"),
        note: z.string().optional().describe("Optional note or description for the redirect"),
      },
      annotations: {
        readOnlyHint: false,
        destructiveHint: false,
        idempotentHint: true,
        openWorldHint: false,
      },
    },
    async (args) => {
      try {
        const { id, ...body } = args;
        const data = await client.put(`/redirects/${id}`, body);
        return {
          content: [{ type: "text" as const, text: truncateResponse(data) }],
        };
      } catch (err) {
        const message = err instanceof Error ? err.message : String(err);
        return {
          content: [{ type: "text" as const, text: `Error: ${message}` }],
          isError: true,
        };
      }
    }
  );

  server.registerTool(
    "nksweb_delete_redirect",
    {
      title: "Delete Redirect",
      description: "Permanently delete a URL redirect by ID",
      inputSchema: {
        id: z.number().describe("Redirect ID to delete"),
      },
      annotations: {
        readOnlyHint: false,
        destructiveHint: true,
        idempotentHint: false,
        openWorldHint: false,
      },
    },
    async (args) => {
      try {
        const data = await client.delete(`/redirects/${args.id}`);
        return {
          content: [{ type: "text" as const, text: truncateResponse(data) }],
        };
      } catch (err) {
        const message = err instanceof Error ? err.message : String(err);
        return {
          content: [{ type: "text" as const, text: `Error: ${message}` }],
          isError: true,
        };
      }
    }
  );
}
